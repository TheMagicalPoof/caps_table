type TranslationKey = keyof typeof translations.ru;
type LayoutTranslationKey = keyof typeof translations.ru.layout;

export function detectBrowserLocale(): Locale {
  if (typeof navigator === 'undefined') return 'ru';

  for (const language of navigator.languages ?? [navigator.language]) {
    const locale = language.toLowerCase().split('-')[0];
    if (supportedLocales.includes(locale as Locale)) return locale as Locale;
  }

  return 'en';
}

export function interpolate(template: string, values: Record<string, string | number> = {}) {
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(values[key] ?? ''));
}

export function t(locale: Locale, key: TranslationKey, values?: Record<string, string | number>) {
  const value = translations[locale][key] ?? translations.ru[key];
  if (typeof value !== 'string') return '';
  return interpolate(value, values);
}

export function tl(locale: Locale, key: LayoutTranslationKey, values?: Record<string, string | number>) {
  return interpolate(translations[locale].layout[key] ?? translations.ru.layout[key], values);
}

export function formatCandidateCount(locale: Locale, count: number) {
  const labels = translations[locale].candidates;
  const mod10 = count % 10;
  const mod100 = count % 100;
  const label =
    locale === 'ru' && mod10 === 1 && mod100 !== 11
      ? labels.one
      : locale === 'ru' && mod10 >= 2 && mod10 <= 4 && (mod100 < 12 || mod100 > 14)
        ? labels.few
        : labels.many;

  return locale === 'ja' ? `${count}${label}` : `${count} ${label}`;
}
